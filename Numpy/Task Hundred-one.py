import numpy as np
x = np.array([3,5])
y = np.array([2,6])
print('The original lists are: ')
print(x)
print(y)
print('Comparison - greater')
print(np.greater(x, y))
print('Comparison - greater')
print(np.greater_equal(x, y))
print('Comparison - less')
print(np.less(x,y))
print("Comparision - less_equal")
print(np.less_equal(x,y))


